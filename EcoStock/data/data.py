# Function to fetch data from the World Bank API
def fetch_data(country, indicator):
    url = f"http://api.worldbank.org/v2/country/{country}/indicator/{indicator}?date={start_date}:{end_date}&format=json"
    r = requests.get(url)
    if r.status_code == 200:
        data = r.json()
        if data[1]:  # check if data is not empty
            df = pd.DataFrame(data[1])
            df['indicator'] = df['indicator'].apply(lambda x: x['value'])
            df['country'] = df['country'].apply(lambda x: x['value'])
            df = df.drop(columns=['obs_status', 'unit', 'decimal'])  # drop unnecessary columns
            df = df.dropna(subset=['value'])  # drop rows with NA values in 'value' column
            return df
        else:
            return pd.DataFrame()  # return an empty DataFrame if data is empty

# Initialize an empty DataFrame
df = pd.DataFrame()

# Use a ThreadPoolExecutor to make the requests in parallel
with concurrent.futures.ThreadPoolExecutor() as executor:
    # Create a list of future tasks
    futures = [executor.submit(fetch_data, country, indicator) for country in countries for indicator in indicators]
    
    # Loop through the futures and add the results to the DataFrame
    for future in concurrent.futures.as_completed(futures):
        df = pd.concat([df, future.result()])

# Save the DataFrame as a .csv file
df.to_csv('world_bank_data.csv', index=False)

