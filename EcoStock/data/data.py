# Function to download stock data and return a DataFrame
def get_stock_df(stock_name):
    # Download the stock data into a DataFrame
    stock_data = yf.download(stock_name, start='2010-01-01', end='2021-01-01')

    # Check if the index is a DateTime. If not, convert it to a DateTime
    if not isinstance(stock_data.index, pd.DatetimeIndex):
        stock_data.index = pd.to_datetime(stock_data.index)
    
    # Add a 'Stock' column to the DataFrame
    stock_data['Stock'] = stock_name
    
    # Resample the data to get the last trading day of each year
    stock_data = stock_data.resample('Y').last()
    
    return stock_data

